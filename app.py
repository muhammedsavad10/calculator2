"""Basic calculator application."""


def add(a,b):
    """Return the value added of 2 variables"""
    return a+b

def subtract(a,b):
    """Retun the value subtracted from 2 values"""
    return a-b

def multiply(a,b):
    """Return the value multiplied between 2 variables"""
    return a*b

def divide(a,b):
    """return the value recieved by dividing 2 numbers"""
    return a/b

def power(a,b):
    """Return the power value"""
    return a**b
